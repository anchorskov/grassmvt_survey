-- Migration 0020: Add Wyoming legislators table
-- Purpose: Store Wyoming state legislators with contact info for district context display

CREATE TABLE IF NOT EXISTS wy_legislators (
  voter_id TEXT,
  name TEXT NOT NULL,
  chamber TEXT NOT NULL,  -- 'House' or 'Senate'
  district TEXT NOT NULL,
  city TEXT,
  county TEXT,
  party TEXT,
  affiliations TEXT,
  campaign_website TEXT,
  official_profile_url TEXT,
  phone TEXT,
  email TEXT,
  updated TEXT
);

CREATE INDEX idx_wy_leg_chamber_district ON wy_legislators(chamber, district);
CREATE INDEX idx_wy_leg_state ON wy_legislators(chamber);

